    /**
     * 下载文件 并返回文件路径
     */
    public static String download(String url, String filePath, String fileName, String suffix) {
        String savePath = filePath + fileName + suffix;
        File directory = new File(filePath);
        if (!directory.exists()) {
            directory.mkdirs();
        }
        // 下载文件保存路径
        CloseableHttpClient httpclient = HttpClients.createDefault();
        CloseableHttpResponse response = null;
        try {
            // 使用URIBuilder对象用于设置请求参数
            URIBuilder urlBuilder = new URIBuilder(url);
            urlBuilder.addParameter("fileName", fileName);
            HttpGet httpGet = new HttpGet(urlBuilder.build());
            // 执行请求
            response = httpclient.execute(httpGet);
            if (response.getStatusLine().getStatusCode() == 200) {
                HttpEntity entity = response.getEntity();
                InputStream is = entity.getContent();
                FileOutputStream fos = new FileOutputStream(savePath);
                int len = 0;
                byte[] b = new byte[1024];
                while ((len = is.read(b)) != -1) {
                    fos.write(b, 0, len);
                }
                fos.close();
            }
        } catch (Exception e) {
            e.getStackTrace();
        } finally {
            try {
                response.close();
                httpclient.close();
            } catch (IOException e) {
                e.getStackTrace();
            }
        }
        return savePath;

    }

